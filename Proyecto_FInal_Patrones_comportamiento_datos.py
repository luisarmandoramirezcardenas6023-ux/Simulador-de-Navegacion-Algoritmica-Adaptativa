def calcular_distancia(coord1, coord2):
    return math.hypot(coord1[0] - coord2[0], coord1[1] - coord2[1])


def crear_grafo(coordenadas, enlaces):
    G = nx.Graph()
    for i, coord in enumerate(coordenadas):
        G.add_node(i, pos=(coord[1], coord[0]))
    for u, v in enlaces:
        if u < len(coordenadas) and v < len(coordenadas):
            G.add_edge(u, v, weight=calcular_distancia(coordenadas[u], coordenadas[v]))
    return G


def obtener_bloqueos_aleatorios(G, cant, excluir=[]):
    aristas = [e for e in G.edges() if e not in excluir and (e[1], e[0]) not in excluir]
    random.shuffle(aristas)
    bloqueos = []
    nodos_afectados = set()

    for e in aristas:
        u, v = e
        if 184 in (u, v) or 0 in (u, v):
            continue

        if u in nodos_afectados or v in nodos_afectados:
            continue

        bloqueos.append(e)
        nodos_afectados.add(u)
        nodos_afectados.add(v)

        if len(bloqueos) == cant:
            break

    return bloqueos


